    private void rSMaterialButtonRectangle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle1ActionPerformed
        // TODO add your handling code here:
        String kodealat = txtkode.getText();
        String namaalat = txtnama.getText();
        String stock = txtstock.getText();
        int stoke = Integer.parseInt(stock);
        String merek = txtmerek.getText();
        String harga = txtharga.getText();
        int rego = Integer.parseInt(harga);

        if (kodealat.isEmpty() || namaalat.isEmpty() || stock.isEmpty() || merek.isEmpty() || harga.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua wajib diisi");
        } else {
            try(Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uas_pbo2", "root", "")) {
                String sql = "INSERT INTO tb_data_alat ("
                        + "kode_alat, nama_alat, stock, merek, harga)"
                        + "VALUES (?, ?, ?, ?, ?)";

                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, kodealat);
                statement.setString(2, namaalat);
                statement.setInt(3, stoke);
                statement.setString(4, merek);
                statement.setInt(5, rego);

                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
      
                dispose();
                new FAlat().setVisible(true);

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_rSMaterialButtonRectangle1ActionPerformed

