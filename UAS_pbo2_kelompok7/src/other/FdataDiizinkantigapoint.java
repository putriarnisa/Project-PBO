    public void tampil() {
        String cabang = SESSION.SessionCabang.getSessionCabang();
        String st = SESSION.SessionStatus2.getSessionStatus2();
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_uas_pbo2", "root", "")) {
            String sql = "SELECT * FROM tb_pengelolaan_cabang WHERE Cabang = ? AND status = ? AND status2 = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, cabang);
            stmt.setString(2, "Di_izinkan");
            stmt.setString(3, st);

            System.out.println(cabang);
            System.out.println(st);

            ResultSet rs = stmt.executeQuery();

            // Menghapus data sebelumnya dari model tabel
            DefaultTableModel tableModel = (DefaultTableModel) tampill.getModel();
            tableModel.setRowCount(0);
            tableModel.setColumnCount(0);
            tableModel.addColumn("ID cabang");
            tableModel.addColumn("ID gudang");
            tableModel.addColumn("Kode Alat");
            tableModel.addColumn("Nama Alat");
            tableModel.addColumn("Jumlah");
            tableModel.addColumn("Cabang");
            tableModel.addColumn("Status");
            tableModel.addColumn("Status2");

            // Mengisi data ke model tabel
            while (rs.next()) {
                String id = rs.getString("id_cabang");
                String id_gudang = rs.getString("id_gudang");
                String kodeAlat = rs.getString("kode_alat");
                String namaAlat = rs.getString("nama_alat");
                int jumlah = rs.getInt("jumlah");
                String Cabang2 = rs.getString("Cabang");
                String status = rs.getString("status");
                String status2 = rs.getString("status2");

                Object[] rowData = {id, id_gudang, kodeAlat, namaAlat, jumlah, Cabang2, status, status2};
                tableModel.addRow(rowData);
            }

            // Set model tabel pada objek JTable
            tampill.setModel(tableModel);

            // Mengatur lebar kolom
            TableColumnModel columnModel = tampill.getColumnModel();
            columnModel.getColumn(4).setPreferredWidth(10); // Kolom Jumlah

            // Mengatur tinggi baris
            tampill.setRowHeight(30); // Tinggi baris menjadi 30 piksel
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

