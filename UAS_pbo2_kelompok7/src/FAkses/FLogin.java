    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String user = txtuser.getText();
        String password = textpw.getText();

        if (user.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Wajib di isi semua");

        } else {
            try {
                Connection con = Koneksi.KoneksiDatabase.getKoneksi();
                Statement st = con.createStatement();
                String sql = "Select * from user where username = '" + txtuser.getText()
                        + "' and password = '" + textpw.getText() + "'";
                ResultSet rs = st.executeQuery(sql);
                if (rs.next()) {
                    String pass = rs.getString("password");
                    String status = rs.getString("level");

                    if (pass.equals(pass) && user.equals(user)) {
                        JOptionPane.showMessageDialog(this, "Login Berhasil");
                        SESSION.SessionLevel.setSessionLevel(status);
                        //                        idSession.setidUSerLogin(idUSer);
                        if (status.equals("Admin")) {
                            FAkses.FMenu mu = new FAkses.FMenu();
                            mu.setVisible(true);
                            dispose();
                        } else if (status.equals("Gudang")) {
                            FAkses.FMenu mu = new FAkses.FMenu();
                            mu.setVisible(true);
                            dispose();
                        } else if (status.equals("Direktur")) {
                            FAkses.FMenu mu = new FAkses.FMenu();
                            mu.setVisible(true);
                            dispose();
                        } else {
                            FAkses.FMenu mu = new FAkses.FMenu();
                            mu.setVisible(true);
                            dispose();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Username atau Password anda salah");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Username atau Password anda salah");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat melakukan login");
                System.out.println("Error : " + e);
            }
        }
    }//GEN-LAST:event_loginActionPerformed

